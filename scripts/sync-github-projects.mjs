#!/usr/bin/env node
import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const projectRoot = fileURLToPath(new URL('..', import.meta.url));
const configPath = path.join(projectRoot, 'src', 'data', 'projects.config.json');
const outputPath = path.join(projectRoot, 'src', 'data', 'generated', 'github-projects.ts');
const strict = process.env.GITHUB_PROJECTS_STRICT === '1';

async function main() {
  const config = JSON.parse(await fs.readFile(configPath, 'utf8'));
  const owner = config.owner || 'jsw-teams';
  const repositories = await fetchPublicRepositories(owner);
  const projects = normalizeRepositories(repositories);

  await fs.mkdir(path.dirname(outputPath), { recursive: true });
  await fs.writeFile(outputPath, renderTypeScript(projects), 'utf8');

  console.log(`Synced ${projects.length} GitHub public projects for ${owner}.`);
}

async function fetchPublicRepositories(owner) {
  const token = process.env.GITHUB_TOKEN || process.env.GH_TOKEN || '';
  const headers = {
    Accept: 'application/vnd.github+json',
    'X-GitHub-Api-Version': '2022-11-28',
    'User-Agent': 'js-gripe-site-build'
  };

  if (token) {
    headers.Authorization = `Bearer ${token}`;
  }

  const items = [];

  for (let page = 1; page <= 10; page += 1) {
    const url = new URL(`https://api.github.com/users/${owner}/repos`);
    url.searchParams.set('type', 'owner');
    url.searchParams.set('sort', 'pushed');
    url.searchParams.set('direction', 'desc');
    url.searchParams.set('per_page', '100');
    url.searchParams.set('page', String(page));

    const response = await fetch(url, { headers });

    if (!response.ok) {
      const body = await response.text();
      throw new Error(`GitHub API ${response.status} ${response.statusText}: ${body.slice(0, 500)}`);
    }

    const pageItems = await response.json();
    if (!Array.isArray(pageItems) || pageItems.length === 0) break;

    items.push(...pageItems);
    if (pageItems.length < 100) break;
  }

  return items;
}

function normalizeRepositories(repositories) {
  return repositories
    .filter((repo) => repo && repo.private === false)
    .filter((repo) => repo.fork === false)
    .filter((repo) => repo.archived === false)
    .map((repo) => ({
      name: repo.name,
      fullName: repo.full_name,
      description: repo.description || '',
      url: repo.html_url,
      homepage: normalizeHomepage(repo.homepage),
      language: repo.language || '',
      topics: Array.isArray(repo.topics) ? repo.topics : [],
      stars: Number(repo.stargazers_count || 0),
      forks: Number(repo.forks_count || 0),
      pushedAt: repo.pushed_at || '',
      updatedAt: repo.updated_at || '',
      archived: Boolean(repo.archived),
      source: 'github'
    }))
    .sort((left, right) => {
      return Date.parse(right.pushedAt || right.updatedAt || 0) - Date.parse(left.pushedAt || left.updatedAt || 0)
        || left.name.localeCompare(right.name, 'en');
    });
}

function normalizeHomepage(value) {
  const homepage = typeof value === 'string' ? value.trim() : '';
  if (!homepage) return '';

  try {
    return new URL(homepage).toString();
  } catch {
    return '';
  }
}

function renderTypeScript(projects) {
  return `// This file is generated by scripts/sync-github-projects.mjs during prebuild.\n` +
    `// Do not edit it manually. Project data comes only from GitHub API.\n\n` +
    `export type GeneratedGitHubProject = {\n` +
    `  name: string;\n` +
    `  fullName: string;\n` +
    `  description: string;\n` +
    `  url: string;\n` +
    `  homepage?: string;\n` +
    `  language?: string;\n` +
    `  topics: string[];\n` +
    `  stars: number;\n` +
    `  forks: number;\n` +
    `  pushedAt: string;\n` +
    `  updatedAt: string;\n` +
    `  archived: boolean;\n` +
    `  source: 'github';\n` +
    `};\n\n` +
    `export const githubProjects: GeneratedGitHubProject[] = ${JSON.stringify(projects, null, 2)};\n` +
    `export const githubProjectsGeneratedAt = ${JSON.stringify(new Date().toISOString())};\n`;
}

main().catch(async (error) => {
  console.error(error);

  if (strict) {
    process.exitCode = 1;
    return;
  }

  try {
    await fs.access(outputPath);
    console.warn('Keeping existing generated GitHub project data because strict mode is disabled.');
  } catch {
    await fs.mkdir(path.dirname(outputPath), { recursive: true });
    await fs.writeFile(outputPath, renderTypeScript([]), 'utf8');
  }
});
